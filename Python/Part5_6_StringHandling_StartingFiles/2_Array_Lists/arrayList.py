import array as arr  # arr is an alias for array


# Array is fixed (static) data structure that holds items of the same data type under one name.
# The contents can be changed, but items cannot be added or deleted.
# The structure must remain fixed

# The variable arrayNums hold an array data structur
arrayNums = [1, 2, 3, 4, 5, 6]  # interms of implementation it is an array
print(arrayNums)
print(type(arrayNums))

# A list is often used as a dynamic data structure, you can add and delete items
#  as well as perform other operations on a list that are different to an array.
aList = ["A", "B", "C", 4, 5, 6, True, 12.5]
print(aList)
print(type(aList))


